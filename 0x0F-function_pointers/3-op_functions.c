/**
  *op_add - Sum
  *@a: first variable
  *@b: second variable
  *Return: sum.
  */
int op_add(int a, int b)
{
int sum;

sum = a + b;
return (sum);
}
/**
  *op_sub - rest
  *@a: first variable
  *@b: second variable
  *Return: rest.
  */
int op_sub(int a, int b)
{
int rest;

rest = a - b;
return (rest);
}
/**
  *op_mul - Mul
  *@a: first variable
  *@b: second variable
  *Return: mul.
  */
int op_mul(int a, int b)
{
int mul;

mul = a * b;
return (mul);
}
/**
  *op_div - div
  *@a: first variable
  *@b: second variable
  *Return: div.
  */
int op_div(int a, int b)
{
int div;

div = a / b;
return (div);
}
/**
  *op_mod - mod
  *@a: first variable
  *@b: second variable
  *Return: mod.
  */
int op_mod(int a, int b)
{
int mod;

mod = a % b;
return (mod);
}
